import axios from 'axios';

import { ICurrencyPairMapping } from '@/shared/model/currency-pair-mapping.model';

const baseApiUrl = 'api/currency-pair-mappings';

export default class CurrencyPairMappingService {
  public find(id: number): Promise<ICurrencyPairMapping> {
    return new Promise<ICurrencyPairMapping>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: ICurrencyPairMapping): Promise<ICurrencyPairMapping> {
    return new Promise<ICurrencyPairMapping>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: ICurrencyPairMapping): Promise<ICurrencyPairMapping> {
    return new Promise<ICurrencyPairMapping>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: ICurrencyPairMapping): Promise<ICurrencyPairMapping> {
    return new Promise<ICurrencyPairMapping>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
