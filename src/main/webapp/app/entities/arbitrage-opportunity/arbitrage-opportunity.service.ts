import axios from 'axios';

import { IArbitrageOpportunity } from '@/shared/model/arbitrage-opportunity.model';

const baseApiUrl = 'api/arbitrage-opportunities';

export default class ArbitrageOpportunityService {
  public find(id: number): Promise<IArbitrageOpportunity> {
    return new Promise<IArbitrageOpportunity>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IArbitrageOpportunity): Promise<IArbitrageOpportunity> {
    return new Promise<IArbitrageOpportunity>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IArbitrageOpportunity): Promise<IArbitrageOpportunity> {
    return new Promise<IArbitrageOpportunity>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IArbitrageOpportunity): Promise<IArbitrageOpportunity> {
    return new Promise<IArbitrageOpportunity>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
